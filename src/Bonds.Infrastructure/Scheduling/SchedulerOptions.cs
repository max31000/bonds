namespace Bonds.Infrastructure.Scheduling;

/// <summary>
/// Параметры автосинка (plan/07 Часть B, spec §3/§11: "1-2 запуска в первые часы торгов MOEX",
/// "Realtime не требуется"). Дефолты — самостоятельное решение (задание прямо называет это
/// "разумной эвристикой, не точной биржевой практикой"): MOEX основная секция торгов открывается
/// ~10:00 MSK; 10:05 — даёт бирже 5 минут на публикацию первых котировок/Gcurve; 14:00 — второй
/// проход в середине дня перехватывает интрадей-изменения (новые операции в T-Invest, сплит
/// купонов). Не привязано к официальному расписанию торговых сессий MOEX — если потребуется
/// точная синхронизация с биржевым календарём, нужно скорректировать здесь, не в коде планировщика.
/// </summary>
public sealed class SchedulerOptions
{
    /// <summary>Времена запуска автосинка по московскому времени (Europe/Moscow). Дефолт — 1-2 запуска в начале торгов MOEX (spec §3).</summary>
    public TimeOnly[] DailySyncTimesMsk { get; set; } = [new TimeOnly(10, 5), new TimeOnly(14, 0)];

    /// <summary>Как часто опрашивать "наступило ли окно автосинка" — не realtime (spec §11), достаточно нескольких раз в час.</summary>
    public TimeSpan PollingInterval { get; set; } = TimeSpan.FromMinutes(5);
}
