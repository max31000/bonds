namespace Bonds.Infrastructure.Services;

/// <summary>
/// Источник токена T-Invest для синка (plan/00 §7, plan/08 "Токен T-Invest через UI") —
/// единая точка, через которую <c>TInvestPortfolioClient</c>/DI должны бы резолвить токен на
/// будущих этапах. На этом этапе используется напрямую эндпоинтом настроек для определения
/// статуса "задан/не задан" и валидации нового токена перед сохранением; коннектор T-Invest
/// (этап 04) продолжает читать "TInvest:Token" из IConfiguration напрямую — переключение
/// коннектора на этот провайдер не входит в скоуп этапа 08 (коннектор уже зарегистрирован и
/// протестирован с текущим способом, а отдельная миграция вызвала бы риск регрессии без
/// явного требования; см. финальный отчёт этапа, "ТРЕБУЕТ СОГЛАСОВАНИЯ" при необходимости).
/// <para>
/// Приоритет источника (plan/00 §7, plan/08 §"Токен T-Invest через UI"): значение из БД
/// (<see cref="GetTokenAsync"/>, override личного использования), если задано — иначе ENV
/// (TInvest:Token) для прод/CI.
/// </para>
/// </summary>
public interface ITInvestTokenProvider
{
    /// <summary>
    /// Возвращает действующий токен с учётом приоритета БД → ENV. Null, если ни один источник
    /// не задан (например, чистая инсталляция до первого онбординга).
    /// </summary>
    Task<string?> GetTokenAsync(CancellationToken ct = default);
}
